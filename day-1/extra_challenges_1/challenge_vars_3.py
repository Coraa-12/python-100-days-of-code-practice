# Formatted Address:

# 1. Ask the user for their street address, city, and postal code.
# 2. Print the address in a neatly formatted way, with each piece of information on a new line. For example:
# [Street Address]
# [City]
# [Postal Code]

street_address = input("What's your street address? ")
city = input("What's the city you're currently living in? ")
postal_code = input("Your postal code? ")

print(street_address + "\n" + city + "\n" + postal_code)